def printPolyomino(polyomino):
    n = polyomino["count"]
    f = [[0 for _ in range(-n, n + 1)] for _ in range(-n, n + 1)]

    for i, j in polyomino["form"]:
        f[n + i][n + j] = 1

    for rows in f:
        for cell in rows:
            if cell == 1:
                print("■", end="")
            else:
                print("□", end="")
        print()


